    private void btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariActionPerformed
        // TODO add your handling code here:
        try{
              String Id = txtidP.getText();
        
        if(Id.length()>0){
            Statement st = Lconnection.createStatement();
            ResultSet rs = st.executeQuery
            ("Select * from tbresep" + " where id='" + txtidP.getText() +"'");
            
            if(rs.next()){
                String dokter = rs.getString(2);
                String tanggal = rs.getString(3);
                String resep = rs.getString(4);
                
                txtdokter.setText(dokter);
                txttanggal.setText(tanggal);
                txtresep.setText(resep);
                
            } else{
                JOptionPane.showMessageDialog(rootPane, "ID tidak ada", "Pesan", JOptionPane.ERROR_MESSAGE);
                txtidP.setText("");
            }
        }  
        
        }catch(Exception ex){
                JOptionPane.showMessageDialog(rootPane, "SQL synatx should be reviewed", "Pesan", JOptionPane.ERROR_MESSAGE);
                txtidP.setText("");
        }
    }//GEN-LAST:event_btncariActionPerformed

