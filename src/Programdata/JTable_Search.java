    public ArrayList<User> ListUser (String ValToSearch){
        
        ArrayList<User> userList = new ArrayList<User>();
        Statement st;
        ResultSet rs;
        
        try{
            Connection con = getConnection();
            st = con.createStatement();
            String searchQuery = "SELECT * FROM `tbpasien` WHERE CONCAT (`id`, `nama`, `umur`, `alamat`, `No_Telepon`) LIKE '%"+ValToSearch+"%'";
            rs = st.executeQuery(searchQuery);
            
            User user;
            
            while(rs.next()){
                user = new User(
                        rs.getInt("id"),
                        rs.getString("nama"),
                        rs.getString("umur"),
                        rs.getString("No_Telepon"),
                        rs.getString("alamat")
                );
                
                userList.add(user);
            }
            
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
        return userList;
    }
    
    public void findUsers(){
        ArrayList<User> users = ListUser(txtsearch.getText());
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers (new Object[]{"Id","Nama","Umur","Alamat","Telepon"});
        Object[] row = new Object [5];
        
        for(int i=0;i<users.size(); i++){
            row[0] = users.get(i).getId();
            row[1] = users.get(i).getnama();
            row[2] = users.get(i).getumur();
            row[3] = users.get(i).getalamat();
            row[4] = users.get(i).gettelepon();
            
            model.addRow(row);
        }
        jTable_Users.setModel(model);
    }
    
    
