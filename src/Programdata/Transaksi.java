    private void btninputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninputActionPerformed
        // TODO add your handling code here:
        String NamaObat = txtobat.getText();
        int HargaObat = Integer.parseInt(txtharga.getText());
        int QTY = Integer.parseInt(txtqty.getText());
        int totalObat = HargaObat * QTY;
        
        DefaultTableModel model = (DefaultTableModel) table_obat.getModel();
        model.addRow(new Object[]{
            NamaObat,
            "Rp. "+HargaObat,
            QTY,
            "Rp. "+totalObat
        });
        int TotalHarga = 0;
        for(int i=0; i<table_obat.getRowCount();i++){
            TotalHarga += Integer.parseInt(table_obat.getValueAt(i,3).toString().substring(4));
        }
        txttotal.setText("Rp. "+TotalHarga);   
        hapuslayar();
        
    }//GEN-LAST:event_btninputActionPerformed

    private void btnbayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbayarActionPerformed
        // TODO add your handling code here:
        int TotalHarga = Integer.parseInt(txttotal.getText().substring(4));
        int Bayar = Integer.parseInt(txtpembayaran.getText());
        int Kembalian = Bayar - TotalHarga;
        
        txtkembalian.setText("Rp. "+Kembalian);
        JOptionPane.showMessageDialog(this,
            "Transaksi Berhasil", "Informasi",
            JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_btnbayarActionPerformed

