    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here: 
        try {
            PreparedStatement pStatement =
            Lconnection.prepareStatement("UPDATE tbpasien SET nama=?,umur=?,alamat=?,No_Telepon=? WHERE id=?");
            pStatement.setString(1, txtnamaP.getText());
            pStatement.setString(2, txtusiaP.getText());
            pStatement.setString(3, txtalamatP.getText());
            pStatement.setString(4, txtteleponP.getText());
            pStatement.setString(5, txtidP.getText());

            if (pStatement.executeUpdate()>0)
                JOptionPane.showMessageDialog(this,
                "Edit sukses", "Informasi",
                JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(this,"Edit gagal", "Informasi",JOptionPane.INFORMATION_MESSAGE);

            pStatement.close();
            
            txtnamaP.setText("");
            txtusiaP.setText("");
            txtteleponP.setText("");
            txtalamatP.setText("");
            txtidP.setText("");
        }
        catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

