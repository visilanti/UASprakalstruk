    private Object[][] getData(){
    Object[][] data1= null;
    
        try {
        Statement st = Lconnection.createStatement();
        ResultSet rs = st.executeQuery("Select * from tbpasien");
        rs.last();
        int rowCount= rs.getRow();
        rs.beforeFirst();
        data1= new Object[rowCount][4];
        int no=-1;
        while (rs.next()) {
            no=no+1;
            data1[no][0]=rs.getString("nama");
            data1[no][1]=rs.getString("umur");
            data1[no][2]=rs.getString("No_Telepon");
            data1[no][3]=rs.getString("alamat");
        }
        
        st.close();
    }
    catch (SQLException e){
        System.out.println("koneksi gagal " +e.toString());
    }
        return data1;
    }
    
