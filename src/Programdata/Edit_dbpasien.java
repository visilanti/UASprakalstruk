    private void btncekidPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncekidPActionPerformed
        // TODO add your handling code here:
         // TODO add your handling code here:
        try { 
            Statement st = Lconnection.createStatement();
            ResultSet rs = st.executeQuery
            ("Select * from tbpasien" + " where id='" + txtidP.getText() +"'");
            if (rs.next()) {
                btnsimpan.setEnabled(true);
                txtnamaP.setText(rs.getString("nama"));
                txtusiaP.setText(rs.getString("umur"));
                txtteleponP.setText(rs.getString("No_Telepon"));
                txtalamatP.setText(rs.getString("alamat"));
                txtidP.setText(rs.getString("id"));
            } else {
                JOptionPane.showMessageDialog(this,"ID: Salah",
                "Informasi", JOptionPane.INFORMATION_MESSAGE);
            
            btnsimpan.setEnabled(false);
            txtnamaP.setText("");
            txtusiaP.setText("");
            txtteleponP.setText("");
            txtalamatP.setText("");
            txtnamaP.requestFocus();
            }
        }
        catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());
        }
    }//GEN-LAST:event_btncekidPActionPerformed

